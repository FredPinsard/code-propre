package ex1;

/** Classe proposant un certain nombre d'operations
 *
 * @author Pinsard
 */
public class Calcul {

	/** retourn l'addition de deux entiers
	 * @param a
	 * @param b
	 * @return int
	 */
	public int addition(int a, int b){
		return a+b;
	}

	/** retourne la soustraction de deux entiers
	 * @param a
	 * @param b
	 * @return int
	 */
	public int soustraction(int a, int b){
		return a-b;
	}
}
